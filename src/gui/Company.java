    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String name = jTextField1.getText();
        String mobile = jTextField2.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!mobile.matches("^(?:0|94|\\+94|0094)?(?:(11|21|23|24|25|26|27|31|32|33|34|35|36|37|38|41|45|47|51|52|54|55|57|63|65|66|67|81|91)(0|2|3|4|5|7|9)|7(0|1|2|4|5|6|7|8)\\d)\\d{6}$")) {
            JOptionPane.showMessageDialog(this, "Please enter valid mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `company` WHERE `mobile` = '" + mobile + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Company already registered", "Warning", JOptionPane.INFORMATION_MESSAGE);

                } else {

                    MySQL.executeIUD("INSERT INTO `company` (`c_name`,`mobile`) VALUES('" + name + "', '" + mobile + "')");
                    reset();
                    loadCompany();
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String name = jTextField1.getText();
        String mobile = jTextField2.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                MySQL.executeIUD("UPDATE `company` SET `c_name` = '" + name + "' WHERE `mobile` = '" + mobile + "'");
                reset();
                loadCompany();
                JOptionPane.showMessageDialog(this, "Update Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        int row = jTable1.getSelectedRow();

        if (row != -1) {
            int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the selected company?", "Delete Confirmation", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

                try {
                    String id = String.valueOf(jTable1.getValueAt(row, 0));
                    MySQL.executeIUD("DELETE FROM `company` WHERE `id` = '" + id + "'");
                    loadCompany();
                    reset();

                } catch (Exception e) {
                    e.printStackTrace();
                }

                JOptionPane.showMessageDialog(null, "Company removed successfully!");

            } else {

                JOptionPane.showMessageDialog(null, "Deletion canceled.");
            }
        } else {

            JOptionPane.showMessageDialog(null, "Please select a supplier to remove.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

