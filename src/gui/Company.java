    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int row = jTable1.getSelectedRow();

        jTextField1.setText(String.valueOf(jTable1.getValueAt(row, 1)));
        jTextField2.setText(String.valueOf(jTable1.getValueAt(row, 2)));

        jTextField2.setEnabled(false);

        if (evt.getClickCount()== 2) {

            if (grn != null) {
                grn.getjLabel4().setText(String.valueOf(jTable1.getValueAt(row, 1)));
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "GRN Management object is not initialized.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_jTable1MouseClicked

