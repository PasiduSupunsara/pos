    private void loadingAnimation(){
        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 0; i <= 100; i++) {
                    
                    try {
                        Thread.sleep(50);
                        
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }
                    
                }
                splashWindow.dispose();
                new Admin_Cashier().setVisible(true);
            }
        });
        t.start();
    }
