    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        
        String category = String.valueOf(jComboBox1.getSelectedItem());
        String subcategory = jTextField3.getText();
        
        if (category== null || category.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (subcategory.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter SubCategory", "Warning", JOptionPane.WARNING_MESSAGE);
        
        } else  {
            try {
                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `sub_category` WHERE `sub_category` = '" + subcategory + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Product already added", "Success", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.executeIUD("INSERT INTO `sub_category`(`sub_category`,`main_category_id`) VALUES ('" + subcategory + "', '" + Categorymap.get(category) + "') ");
                    loadproduct();
                    reset();
                   
                    JOptionPane.showMessageDialog(this, "New product addedd", "Success", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
        
    }//GEN-LAST:event_jButton8ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        
        int row = jTable1.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a category to update", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            try {
                String id = String.valueOf(jTable1.getValueAt(row, 0));
                String category = String.valueOf(jComboBox1.getSelectedItem());
                String categoryName = jTextField3.getText();

                if (categoryName.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please enter category name", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    boolean isFound = false;

                    for (int i = 0; i < jTable1.getRowCount(); i++) {

                        String getCategoryName = String.valueOf(jTable1.getValueAt(i, 1));

                        if (getCategoryName.equals(categoryName)) {
                            JOptionPane.showMessageDialog(this, "category already added", "Success", JOptionPane.INFORMATION_MESSAGE);
                            isFound = true;
                            break;

                        }

                    }

                    if (!isFound) {
                        MySQL.executeIUD("UPDATE `sub_category` SET `sub_category` = '" + categoryName + "', `main_category_id` = '"+ Categorymap.get(category) +"' WHERE `id` = '" + id + "'");

                        loadproduct();
                        JOptionPane.showMessageDialog(this, "SubCategory Update Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                        
                    }
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

          
        }
        
    }//GEN-LAST:event_jButton7ActionPerformed

